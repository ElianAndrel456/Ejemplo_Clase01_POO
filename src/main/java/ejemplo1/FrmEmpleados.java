    private void jbtnCrearActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        // Capturando informacion de los empleados de los objetos del formulario
        String cod = jtxtCodigo.getText();
        String nom = jtxtNombre.getText();
        String seguro = jcbxSeguro.getSelectedItem().toString();
        double sueldo = Double.parseDouble(jtxtSueldo.getText());
        double h_extras = Double.parseDouble(jtxtHextras.getText());
        String area = jcbxArea.getSelectedItem().toString();

        // Creando el objeto empleado de la clase Empleados
        empleado = new Empleados(cod, nom, area, sueldo, h_extras, seguro);

    }// GEN-LAST:event_jbtnCrearActionPerformed

