    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        double codigo = Double.parseDouble(txtCodigo.getText());
        String nombre = txtNombre.getText();
        String condicionDeContrato = txtcCondicionContrato.getSelectedItem().toString();
        String especialidad = txtcEspecialidad.getSelectedItem().toString();
        String tipoDeSupervicion = txtcTipoDeSupervicion.getSelectedItem().toString();
        int numeroDeObrasAsignadas = Integer.parseInt(txtNumeroObrasAsignadas.getText());
        String tipoSeguro = txtcTipoSeguro.getSelectedItem().toString();

        arquitecto = new Arquitecto(codigo, nombre, condicionDeContrato, especialidad, tipoDeSupervicion,
                numeroDeObrasAsignadas, tipoSeguro);
    }// GEN-LAST:event_btnCrearActionPerformed

