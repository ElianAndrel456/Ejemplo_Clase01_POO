        private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnCrearActionPerformed
                // TODO add your handling code here:
                String nombre = txtNombre.getText();
                String sexo = txtcSexo.getSelectedItem().toString();
                String dni = txtDNI.getText();
                String area = txtcArea.getSelectedItem().toString();
                String cargo = txtcCargo.getSelectedItem().toString();
                double antiguedad = Double.parseDouble(txtAntiguedad.getText());
                String seguro = txtcSeguro.getSelectedItem().toString();

                jefe = new Jefe(nombre, sexo, cargo, area, dni, antiguedad, seguro);
        }// GEN-LAST:event_btnCrearActionPerformed

